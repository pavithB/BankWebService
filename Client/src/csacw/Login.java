    public boolean check(){
        boolean isCheck = false;
        
        if(txtUName.getText().equals("")  || String.valueOf(txtUPw.getText()).equals("")){
            
             JOptionPane.showMessageDialog(null,"UserName & PassWord cannot be EMPTY");
             
            isCheck = true ;
            
        }
        
        return isCheck ;
    }

