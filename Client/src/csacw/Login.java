    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isCheck = check(); 
        if(isCheck){
            
        }else{
        
        String uName = txtUName.getText();
        String uPass = String.valueOf(txtUPw.getText());
        String inMessage = login(uName, uPass);
        String message1 = "Login Successfull!";
        String message2 = "Invalid Login! Please try again";
        if(inMessage.equals(message1)){
            JOptionPane.showMessageDialog(null,message1);
            this.hide();
            CustomerList customerList = new CustomerList();
            customerList.show();
        }
        else{
              JOptionPane.showMessageDialog(null,message2);
        }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

